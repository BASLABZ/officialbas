  class TFrekuensiKar
  {
  	var $FFrek,$FBanyak;
  	
  	function TFrekuensiKar() 
  	{
  		global $FFrek,$FBanyak;
  		
  		for($i=0;$i<256;$i++) $FFrek[$i] = array("Kar" => chr(0), "Frek" => 0);
  		$FBanyak = 0;
  	}
  	
  	function Clear() 
  	{
  		global $FBanyak;
  		
  		$FBanyak = 0;	
  	}

	function CariKar($c)
	{
		global $FFrek,$FBanyak;
		
		$retval = -1;
		for($i=0;$i<$FBanyak;$i++)
		{ 
			if ($c==$FFrek[$i]["Kar"])
			{
				$retval = $i;
				break;
			}
		}
		return $retval;
	}
	
	function Kebalikan($c)
	{
		global $FFrek,$FBanyak;
		
		$retval = $c;
		$i = $this->CariKar($c);
		if ($i>=0) $retval = $FFrek[($FBanyak-1)-$i]["Kar"];	
		return $retval;	
	}
	
	function HitungKarakter($c)
	{
		global $FFrek,$FBanyak;
		
		if ($c==chr(0)) break;
		$i = $this->CariKar($c);
		
		if ($i>=0) $FFrek[$i]["Frek"]++; 
		else 
		{
			$i = $FBanyak;
			$FFrek[$i]["Kar"] = $c;
			$FFrek[$i]["Frek"]= 1;
			$FBanyak++;
		}	
	}
	
	function HitungString($s)
	{
		$this->Clear();
		for($i=1;$i<=strlen($s);$i++) $this->HitungKarakter($s{$i});
	}

	function HitungBuffer($Buf,$ALength)
	{
		$this->Clear();
		for($i=0;$i<$ALength;$i++) $this->HitungKarakter($Buf{$i});		
	}
	
	function Tukar($index1,$index2)
	{
		global $FFrek;
		
		$temp = $FFrek[$index1];
		$FFrek[$index1] = $FFrek[$index2];
		$FFrek[$index2] = $temp;
	}	
	
	function UrutkanFrekuensi()
	{
		global $FFrek,$FBanyak;
		
		for($i=0;$i<($FBanyak-1);$i++)
		for($j=$i;$j<$FBanyak;$j++)
		{
			if ($FFrek[$j]["Frek"] > $FFrek[$i]["Frek"]) $this->Tukar($i, $j);	
		}
	}

  	
  }

