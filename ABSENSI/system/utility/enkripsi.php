function dekripfile($ASourceFile, $ADestFile, $APassword)
{

	$l = strlen($APassword);
	$fp = fopen($ASourceFile, "r");
	$fw = fopen($ADestFile, "w");
	
	$fstat = fstat($fp);
	if ($fstat["size"]<strlen($APassword)) die("File Rusak!");
	$buff = fread($fp,$l);
	dekripbuffer($buff,strlen($buff),'');
	if ($buff!=$APassword) die("File Rusak!");
	while (!feof($fp))
	{
		$buff = fread($fp,4096);
		dekripbuffer($buff,strlen($buff),$APassword);
		fwrite($fw,$buff);
	}
	fclose($fw);
	fclose($fp);
}	


