			$j = $TabelBalik[ord($buf{$i})] - ord($APassword{$l - ($i % $l) -1}) - $i;
			if ($j >= 0) $c = $j;
			else $c = 255-(((-$j)-1) % 256);
			$buf{$i} = chr(255-$c); 
		}
	} else {
		for($i=0;$i<$ALength;$i++)
		{
			$j = $TabelBalik[ord($buf{$i})] - $i;
			if ($j >= 0) $c = $j;
			else $c = 255-(((-$j)-1) % 256);
			$buf{$i} = chr(255-$c); 	
		}		
	}
	
	$Frek->HitungBuffer($buf,$ALength);
	$Frek->UrutkanFrekuensi();
	for($i=0;$i<$ALength;$i++) $buf{$i} = $Frek->Kebalikan($buf{$i});
	
	$buf2 = $buf;
	for($i=0;$i<($ALength);$i++) $buf{$i} = $buf2{$ALength-$i-1};

	
}


